function Script1()
{
  var test = document.getElementById("MyStyle");
if(!test){
 var body = document.querySelector("body");
 var style = document.createElement('style');
 style.type = 'text/css';
 style.id = 'MyStyle';
 style.appendChild(document.createTextNode(".caption p { background: rgba(236,212,76,0.7); color: black; text-decoration: none; text-shadow: none; font-weight: bold; }"));
 body.appendChild(style);
}

if (!window.movedCaptions) {
 var sheet = document.createElement('style');
 sheet.innerHTML = ".caption{margin-bottom:45px !important; margin-left:75px !important }";
/*margin-bottom: 65px - 45px ; margin-left: 90px - 75px*/
 document.body.appendChild(sheet);
 window.movedCaptions = true;
}
}

